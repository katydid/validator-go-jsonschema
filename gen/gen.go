//Package gen contains some internal utilities for code generation
package gen

import (
	"flag"
	"fmt"
	"os"
	"path/filepath"
	"sort"
	"text/template"
)

//CapFirst capitializes the first letter of a string. This function is undefined for non lowercase english alphabet characters.
func CapFirst(s string) string {
	b := []byte(s)
	b[0] ^= ' '
	return string(b)
}

//LowerFirst lowercases the first letter of a string. This function is undefined for non uppercase english alphabet characters.
func LowerFirst(s string) string {
	b := []byte(s)
	b[0] |= ('a' - 'A')
	return string(b)
}

var funcMap = template.FuncMap{
	"capFirst":   CapFirst,
	"lowerFirst": LowerFirst,
}

type gen struct {
	name string
	dir  string
}

//NewPackage is given a packageName and returns a function, which when called generates code for a specific package.
//Each call of the returned function is used to generate a specific file.
//Given a template, filename, a list of objects which will each be used to execute the template and some top level imports the file is generated.
func NewPackage(packageName string) func(template string, filename string, objects []interface{}, imports ...string) {
	dir := "."
	flag.Parse()
	args := flag.Args()
	if len(args) == 1 {
		dir = args[0]
	}
	fmt.Fprintf(os.Stderr, "generating code in %s\n", dir)
	g := &gen{packageName, dir}
	return g.gen
}

func (this *gen) gen(tmp string, filename string, objects []interface{}, imports ...string) {
	f, err := os.Create(filepath.Join(this.dir, filename))
	if err != nil {
		panic(err)
	}
	write := func(s string) {
		if _, err := f.Write([]byte(s)); err != nil {
			panic(err)
		}
	}
	write(`// Code generated by ` + this.name + `-gen.` + " DO NOT EDIT.\n")
	write(``)
	write(`package ` + this.name + "\n")
	if len(imports) > 0 {
		sort.Strings(imports)
		write("\nimport (\n")
		for i, imp := range imports {
			write("\t" + imp)
			if i < (len(imports) - 1) {
				write("\n")
			}
		}
		write("\n)\n")
	}
	t := template.Must(template.New("a").Funcs(funcMap).Parse(tmp))
	for _, o := range objects {
		err := t.Execute(f, o)
		if err != nil {
			panic(err)
		}
	}
}
